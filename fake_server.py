def _check_auth(handler: BaseHTTPRequestHandler) -> bool:
    """Returns True if the request passes Basic Auth (or auth is unconfigured)."""
    auth = _CONFIG.get("auth") or {}
    username = auth.get("username")
    if username is None:
        return True
    header = handler.headers.get("Authorization", "")
    if not header.startswith("Basic "):
        return False
    try:
        decoded = base64.b64decode(header[6:]).decode("utf-8", errors="replace")
    except Exception:
        return False
    colon = decoded.find(":")
    if colon < 0:
        return False
    req_user, req_pass = decoded[:colon], decoded[colon + 1 :]
    if req_user != username:
        return False
    return hashlib.sha256(req_pass.encode()).hexdigest() == (auth.get("pass_sha256") or "")


