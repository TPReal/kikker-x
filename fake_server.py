def _load_config() -> dict[str, Any]:
    """
    Loads the merged config into a Python dict.

    Tries src/_config.json first (generated by last build), then falls
    back to configs/default_config.json.  Prints which file was loaded.
    """
    built_config = os.path.join(SCRIPT_DIR, "src", "_config.json")
    default_config = os.path.join(SCRIPT_DIR, "configs", "default_config.json")

    def load(path: str) -> dict[str, Any]:
        with open(path, "r", encoding="utf-8") as f:
            data = json.load(f)
            print(f"Config loaded from {os.path.relpath(path, SCRIPT_DIR)}")
            return data

    try:
        return load(built_config)
    except FileNotFoundError:
        print(f"Built config not found at {os.path.relpath(built_config, SCRIPT_DIR)}, falling back to default config")
        return load(default_config)


