class KikkerXHandler(BaseHTTPRequestHandler):
    protocol_version = "HTTP/1.1"

    def setup(self) -> None:
        self.request.setsockopt(socket.IPPROTO_TCP, socket.TCP_NODELAY, 1)
        super().setup()

    def log_message(self, fmt: str, *args: Any) -> None:
        print(f"  {self.address_string()}  {fmt % args}")

    def _deny_auth(self) -> None:
        self.send_response(401)
        self.send_header("WWW-Authenticate", 'Basic realm="KikkerX"')
        self.send_header("Content-Length", "0")
        self.end_headers()

    def do_GET(self) -> None:
        parsed = urlparse(self.path)
        if parsed.path != "/manifest.json" and not _check_auth(self):
            self._deny_auth()
            return
        path = parsed.path
        qs = parse_qs(parsed.query)

        def qp(key: str, default: str = "") -> str:
            return qs.get(key, [default])[0]

        # --- Static file routing ---
        # /         → index.html
        # /stream   → stream.html   (extensionless paths get .html appended)
        # /foo.mjs  → foo.mjs       (verbatim)
        if path == "/":
            lookup = "index.html"
        else:
            bare = path.lstrip("/")
            if "." not in bare:
                bare += ".html"
            lookup = bare

        content, ctype = load_static(lookup)
        if content is not None:
            body = content
            is_text = (ctype or "").startswith("text/")
            ct = (ctype or "application/octet-stream") + ("; charset=utf-8" if is_text else "")
            self.send_response(200)
            self.send_header("Content-Type", ct)
            self.send_header("Cache-Control", "no-cache")
            self.send_header("Content-Length", str(len(body)))
            self.end_headers()
            self.wfile.write(body)
            return

        # --- API routes ---
        if path == "/api/logs":
            self._send_text(
                "Camera init OK\n"
                "Scanning WiFi...\n"
                "Connecting to HomeNet (RSSI -52)...\n"
                "Connected: HomeNet  IP: 192.168.1.99  RSSI: -52\n"
                "Camera server ready at http://192.168.1.99/ (http://kikker-x.local/)\n"
                "GET /\n"
                "→ 200 index.html\n"
                "GET /style.css\n"
                "→ 200 style.css\n"
                "GET /logo.svg\n"
                "→ 200 logo.svg\n"
                "GET /api/status\n"
                "→ 200 status 4132mV 87% rssi=-52\n"
                "GET /api/led\n"
                "→ 200 led state=off\n"
                "GET /api/logs\n"
                "→ 200 logs 312 bytes\n"
            )

        elif path == "/api/status":
            rssi = -58 + random.randint(-3, 3)
            data: dict[str, Any] = {
                "id": "c0ffeefacade",
                "wifi": {"mode": "station", "ssid": "FakeAP", "ip": "192.168.1.99", "rssi": rssi},
                "version": "1.0.0",
                "features": _features,
            }
            if _features["battery"]:
                voltage = 3850 + random.randint(-15, 15)
                data["battery"] = {"voltage": voltage, "level": 75}
            self._send_json(data)

        elif path == "/api/led":
            if not _features["led"]:
                self.send_error(404)
                return
            self._send_json({"state": _led_state})

        elif path == "/api/streamfps":
            self._send_json({"fps": 18.5, "active": True})

        elif path == "/api/cam/capture.jpg":
            w, h = RESOLUTIONS.get(qp("res", "UXGA").upper(), (1600, 1200))
            jpeg = make_jpeg(w, h, int(qp("brightness", "0")))
            self.send_response(200)
            self.send_header("Content-Type", "image/jpeg")
            self.send_header("Content-Length", str(len(jpeg)))
            self.send_header("Cache-Control", "no-cache")
            self.end_headers()
            self.wfile.write(jpeg)

        elif path == "/api/cam/stream.mjpeg":
            w, h = RESOLUTIONS.get(qp("res", "VGA").upper(), (640, 480))
            self._serve_mjpeg(w, h, int(qp("brightness", "0")))

        else:
            self.send_error(404)

    def do_PATCH(self) -> None:
        if not _check_auth(self):
            self._deny_auth()
            return
        global _led_state
        parsed = urlparse(self.path)
        if parsed.path == "/api/led":
            if not _features["led"]:
                self.send_error(404)
                return
            length = int(self.headers.get("Content-Length", 0))
            body = json.loads(self.rfile.read(length) or b"{}")
            if "state" in body:
                _led_state = bool(body["state"])
            self._send_json({"state": _led_state})
        else:
            self.send_error(405)

    def do_POST(self) -> None:
        if not _check_auth(self):
            self._deny_auth()
            return
        parsed = urlparse(self.path)
        path = parsed.path

        if path == "/api/restart":
            self._send_text("Restarting.")
            import threading

            def _restart() -> None:
                time.sleep(0.5)
                os.execv(sys.executable, [sys.executable] + sys.argv)

            threading.Thread(target=_restart, daemon=True).start()
        elif path == "/api/poweroff":
            self._send_text("Powering off.")
            import threading

            threading.Thread(target=self.server.shutdown, daemon=True).start()
        elif path == "/api/wifi/reconnect":
            self._send_text("Reconnecting to WiFi.")
        elif path == "/api/led/blink":
            if not _features["led"]:
                self.send_error(404)
                return
            length = int(self.headers.get("Content-Length", 0))
            self.rfile.read(length)  # consume body, blink not simulated
            self._send_text("OK")
        else:
            self.send_error(405)

    # ---- helpers ----

    def _send_json(self, data: Any) -> None:
        body = json.dumps(data).encode()
        self.send_response(200)
        self.send_header("Content-Type", "application/json")
        self.send_header("Cache-Control", "no-cache")
        self.send_header("Content-Length", str(len(body)))
        self.end_headers()
        self.wfile.write(body)

    def _send_text(self, text: str) -> None:
        body = text.encode()
        self.send_response(200)
        self.send_header("Content-Type", "text/plain")
        self.send_header("Cache-Control", "no-cache")
        self.send_header("Content-Length", str(len(body)))
        self.end_headers()
        self.wfile.write(body)

    def _serve_mjpeg(self, width: int, height: int, scene: int = 0) -> None:
        self.send_response(200)
        self.send_header("Content-Type", "multipart/x-mixed-replace;boundary=frame")
        self.send_header("Cache-Control", "no-cache")
        self.end_headers()
        try:
            while True:
                jpeg = make_jpeg(width, height, scene)
                self.wfile.write(
                    b"\r\n--frame\r\n"
                    b"Content-Type: image/jpeg\r\n"
                    b"Content-Length: " + str(len(jpeg)).encode() + b"\r\n\r\n" + jpeg
                )
                self.wfile.flush()
                time.sleep(1 / 30)  # ~30 FPS
        except (BrokenPipeError, ConnectionResetError, OSError):
            pass


class ThreadedHTTPServer(ThreadingMixIn, HTTPServer):
    """Each connection runs in its own thread (MJPEG + page loads concurrently)."""

    daemon_threads = True


